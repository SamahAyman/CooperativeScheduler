void hexToAscii(uint8_t n, uint8_t* conv)//4-bit hex value converted to an ascii character
{
	for (int i = 1; i >= 0; i--){
		conv[i] = n % 10 + '0';
		n = n / 10;
	}
}

void initiation(void)
{
	 tempArrd[0] = 0x11;
   tempArrd[1] = 0x12;
	 ctrlBuffer[0] = 0x0E; 
	 ctrlBuffer[1] = 0x3C; 
}
	
void ReadThreshold(void)
{
		//take threshold input
	 HAL_UART_Transmit(&huart1,startMessage, 47, 10);
	 for(int i=0;i<5;i++)
	 {
		 HAL_UART_Receive(&huart1,(uint8_t*)&thresholdInput[i], 1, HAL_MAX_DELAY);
		 HAL_UART_Transmit(&huart1,(uint8_t*)&thresholdInput[i], 1, HAL_MAX_DELAY);
	 }

	 HAL_UART_Transmit(&huart1,&c[1], 2, 10);
	 HAL_UART_Transmit(&huart1,thesholdMessage, 29, 10);
	 HAL_UART_Transmit(&huart1,(uint8_t*)thresholdInput, 5, HAL_MAX_DELAY);
	 HAL_UART_Transmit(&huart1,&c[1], 2, 10);
	 
	 //get the threshold as a decimal numerical value
	 threshold = atof(thresholdInput);
	 
}


void ReadTemp(void)
{

		//send temp register address 11h to read from
		HAL_I2C_Master_Transmit(&hi2c1, 0xD0, &tempArrd[0], 1, 10);
		//read data of register 11h to tempBuff[0]
		HAL_I2C_Master_Receive(&hi2c1, 0xD1, &tempBuff[0], 1, 10);
		//prepare UART output
	
		actual_temp_whole=tempArrd[0]; 
	
		hexToAscii(tempBuff[0], temp);
		HAL_I2C_Master_Transmit(&hi2c1, 0xD0, &tempArrd[1], 1, 10);
		HAL_I2C_Master_Receive(&hi2c1, 0xD1, &tempBuff[1], 1, 10);
	
		actual_temp_decimal= tempArrd[1];
	
		hexToAscii((tempBuff[1] >> 6) * 25, temp+3);
		// transmit time to UART
		HAL_UART_Transmit(&huart2, currentTempMessage, sizeof(currentTempMessage), 9);
		HAL_UART_Transmit(&huart2, temp, sizeof(temp), 9);

		if (actual_temp_whole>threshold)
		{
			alarm=1;
			HAL_UART_Transmit(&huart2, exceeded, sizeof(exceeded), 9);
		}
		else alarm=0; 

		 ReRunMe(600);
}

void ToggleLED(void)
{
		if(alarm)
		{
				HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_0);
		}
		ReRunMe(5);
}






